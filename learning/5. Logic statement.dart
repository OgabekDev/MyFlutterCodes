main() {

  // Logic - if, if/else, if/else - if

  var num = 5;

  if (num == 5) {
    print("This number is five");
  }

  if (num == 3) {
    print("This number is three");
  } else {
    print("This number is not three");
  }

  if (num == 2) {
    print("This number is two");
  } else if (num == 4) {
    print("This number is four");
  } else {
    print("This number neither two nor 4. It is $num");
  }



}