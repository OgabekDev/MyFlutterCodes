main() {
  
  // String
  var firstName = "Ogabek";
  String lastName = "Matyakubov";
  
  print("String: $firstName $lastName");
  
  // Number
  var x = 10;
  int y = 20;

  print("Integer: $x - $y");
  
  var a = 10.4;
  double b = 10.5;

  print("Double: $a - $b");

  // Boolean

  var isTrue = true;
  bool isFalse = false;

  print("Boolean $isTrue - $isFalse");

  // Dynamic
  dynamic something = "This is dynamic";
  print("Dynamic $something");
  something = 1;
  print("Dynamic $something");
  
}